package by.intexsoft.autotest;

public class CalculatorLogic {
    public int add(int first, int second) {
        return first + second;
    }
    public int div(int first, int second) {
        return first / second;
    }
    public int mult(int first, int second) {
        return first * second;
    }
    public int sub(int first, int second) {
        return first - second;
    }
    public double addFract(double first, double second) {
        return first + second;
    }
    public double subFract(double first, double second) {
        return first - second;
    }
}

